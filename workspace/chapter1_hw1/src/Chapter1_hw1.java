
public class Chapter1_hw1 {

	/**This code helps to calculate area
	 * and perimeter of a rectangle
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println("Hello Class");
		System.out.println("\nCalculating the area of"
				+"a rectangle");
		System.out.println("width is 4.5");
		System.out.println("height is 7.9");
		System.out.println("area is width * height");
		System.out.println("area is:");
		System.out.println(4.5 * 7.9);
		
		System.out.println("\nCalculating the perimeter of"
				+"a rectangle");
		System.out.println("width is 4.5");
		System.out.println("height is 7.9");
		System.out.println("area is 2* width + 2 * height");
		System.out.println("perimeter is:");
		System.out.println(2*4.5 + 2*7.9);


	}

}
